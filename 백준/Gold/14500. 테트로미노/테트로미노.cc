#include <iostream>
#include <vector>

using namespace std;

int map[500][500];

int main()
{
    vector<pair<int, int>> arr[] = {
        {{0,0}, {1,0}, {0,1}, {1,1}}, // ㅁ
        {{0,0}, {0,1}, {0,2}, {0,3}}, // ㅣ
        {{0,0}, {1,0}, {2,0}, {3,0}}, // ㅡ
        // L자 가로
        {{0, 0}, {0, 1}, {0, 2}, {1, 2}},
        {{0, 0}, {0, 1}, {0, 2}, {1, 0}},
        {{0, 0}, {0, 1}, {0, 2}, {-1, 2}},
        {{0, 0}, {0, 1}, {0, 2}, {-1, 0}},
        // L자 세로
        {{0, 0}, {1, 0}, {2, 0}, {2, 1}},
        {{0, 0}, {1, 0}, {2, 0}, {0, 1}},
        {{0, 0}, {1, 0}, {2, 0}, {2, -1}},
        {{0, 0}, {1, 0}, {2, 0}, {0, -1}},
        // T
        {{0, 0}, {0, 1}, {0, 2}, {1, 1}},
        {{0, 0}, {1, 0}, {2, 0}, {1, 1}},
        {{0, 0}, {0, 1}, {0, 2}, {-1, 1}},
        {{0, 0}, {1, 0}, {2, 0}, {1, -1}},
        // Z, S
        {{0, 0}, {0, 1}, {1, 1}, {1, 2}},
        {{0, 0}, {1, 0}, {1, 1}, {2, 1}},
        {{0, 0}, {0, 1}, {-1, 1}, {-1, 2}},
        {{0, 0}, {1, 0}, {1, -1}, {2, -1}},
    };
    int n, m, answer = 0;
    cin >> n >> m;

    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < m; j++)
        {
            cin >> map[i][j];
        }
    }

    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < m; j++)
        {
            for (auto& tetromino : arr)
            {
                int sum = 0;
                for (auto& [dx, dy] : tetromino)
                {
                    int x = i + dx;
                    int y = j + dy;

                    if (x < 0 || y < 0 || x >= n || y >= m) break;
                    sum += map[x][y];
                }
                answer = answer > sum ? answer : sum;
            }
        }
    }
    cout << answer;
    return 0;
}
